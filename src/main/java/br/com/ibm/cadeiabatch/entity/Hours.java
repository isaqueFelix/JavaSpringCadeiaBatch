package br.com.ibm.cadeiabatch.entity;

public class Hours {
	
	private String id;
	private String hour;
	public Hours() {
		super();
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getHour() {
		return hour;
	}
	public void setHour(String hour) {
		this.hour = hour;
	}
}
